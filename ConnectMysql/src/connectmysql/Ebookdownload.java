    private void downloadFile(String contentUrl, String title) {
        try {
            URL url = new URL(contentUrl);
            try (InputStream in = url.openStream()) {
                JFileChooser fileChooser = new JFileChooser();
                fileChooser.setSelectedFile(new File(title + ".pdf")); // Set the default file name

                int result = fileChooser.showSaveDialog(this);

                if (result == JFileChooser.APPROVE_OPTION) {
                    File selectedFile = fileChooser.getSelectedFile();
                    try (FileOutputStream outputStream = new FileOutputStream(selectedFile)) {
                        byte[] buffer = new byte[1024];
                        int bytesRead;
                        while ((bytesRead = in.read(buffer)) != -1) {
                            outputStream.write(buffer, 0, bytesRead);
                        }
                    }
                }
            }
        } catch (IOException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "An error occurred while saving the e-book file.");
        }
    }
    
    
