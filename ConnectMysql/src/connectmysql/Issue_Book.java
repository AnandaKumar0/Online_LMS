    private void loadCategories(){
        try{
            Statement statement=connection.createStatement();
            ResultSet resultset=statement.executeQuery("SELECT * from Issue_Book");
            
            tableModel=(DefaultTableModel) table.getModel();
            tableModel.setRowCount(0);
            
            while(resultset.next()){
                int id=resultset.getInt("id");
                String an=resultset.getString("b_name");
                String adr=resultset.getString("m_name");
                String pho=resultset.getString("book");
                String a;
                a = resultset.getString("Date");
                String c=resultset.getString("R_date");
                
                
                tableModel.addRow(new Object[]{id,an,adr,pho,a,c});
            }
        }
        catch(SQLException e){
            jOptionPane1.showMessageDialog(this,"Failed to load categories from the database.");
        }
    }

