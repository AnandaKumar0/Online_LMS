    private void button3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button3ActionPerformed
        // TODO add your handling code here:
        
        button3.addActionListener(new ActionListener(){
            @Override
            public void actionPerformed(ActionEvent e){
                int ser=table.getSelectedRow();
                if(ser!=-1){
                    int con=jOptionPane1.showConfirmDialog(Issue_Book.this,"Are you sure you want to delete this Issue Book?","Confirm Delete",jOptionPane1.YES_NO_OPTION);
                    if(con==jOptionPane1.YES_OPTION){
                        String id=table.getValueAt(ser,0).toString();
                        try{
                            String sql="DELETE FROM Issue_Book WHERE id=?";
                            PreparedStatement statement=connection.prepareStatement(sql);
                            statement.setString(1,id);
                            statement.executeUpdate();
                            statement.close();
                            
                            loadCategories();
                        }
                        catch(SQLException ex){
                            jOptionPane1.showMessageDialog(Issue_Book.this,"Failed to delete the Issue Book.");
                        }
                    }
                }
                else{
                    jOptionPane1.showMessageDialog(Issue_Book.this,"Please select a Book Name to delete.");
                }
            }
        });
    }//GEN-LAST:event_button3ActionPerformed

